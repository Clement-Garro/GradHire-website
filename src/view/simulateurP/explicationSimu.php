<div class="max-w-md mx-auto mt-10 p-6 bg-white rounded-lg shadow-md">
    <h2 class="text-2xl font-bold mb-4">Explication du simulateur de Pstage</h2>

    <p class="mb-4">Le simulateur de Pstage permet de simuler la création d'une convention de stage.</p>

    <div class="mb-4">
        <h3 class="font-bold">Étapes :</h3>
        <ul class="list-disc ml-6">
            <li>Informations personnelles de l'étudiant</li>
            <li>Informations sur l'entreprise</li>
            <li>Informations sur le service d'accueil</li>
            <li>Informations sur le tuteur professionnel</li>
            <li>Informations sur le stage (l'offre)</li>
            <li>Informations sur le tuteur enseignant</li>
            <li>Informations sur le signataire de la convention</li>
        </ul>
    </div>

    <p class="mb-4">Chaque étape est composée de plusieurs champs à remplir.</p>
    <p class="mb-4">Une fois toutes les étapes remplies, vous pouvez visualiser le récapitulatif pour le modifier si
        nécessaire.</p>
    <p class="mb-4">Si tout est correct, vous pouvez valider la simulation.</p>
    <p class="mb-4">Cela vous téléchargera la convention et la transmettra au secrétariat afin qu'il la valide. Ensuite,
        vous pourrez passer sur Pstage.</p>

    <p class="mb-4">Si vous avez des questions, n'hésitez pas à contacter le secrétariat.</p>

    <a href="/simulateur" class="inline-block px-4 py-2 bg-blue-500 text-white rounded hover:bg-blue-600">Commencer le
        simulateur PStage</a>
</div>

